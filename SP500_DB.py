import numpy as np
import csv


with open('constituents-financials.csv', 'r') as file:
    reader = csv.reader(file, delimiter=",")
    prices = []
    cashtag_list = []
    for line in reader:
        prices = prices + [line[3]]
        cashtag_list = cashtag_list + ['$'+line[0]]

prices.pop(0)
cashtag_list.pop(0)


prices = [float(i) for i in prices]

while len(prices) > 400:
    index = np.argmin(prices)
    cashtag_list.pop(index)
    prices.pop(index)




        
